    //método para adicionar produtos
    private void adicionar(){
        String sql = "insert into produtos(id_produtos, Nome, Marca, Venda, Custo, Quantidade) values(?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            pst.setString(2, txtNome.getText());
            pst.setString(3, txtMarca.getText());
            pst.setString(4, txtVenda.getText());
            pst.setString(5, txtCusto.getText());
            pst.setString(6, txtQuant.getText());
            
//validação dos campos obrigatótios
        if(txtNome.getText().isEmpty()||txtCusto.getText().isEmpty()||txtQuant.getText().isEmpty()||txtVenda.getText().isEmpty()||txtMarca.getText().isEmpty()||txtId.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } else { int adicionado = pst.executeUpdate();
        
        if(adicionado > 0){ 
            JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso");
            
            txtId.setText(null);
            txtNome.setText(null);
            txtCusto.setText(null);
            txtQuant.setText(null);
            txtVenda.setText(null);
            txtMarca.setText(null);
         }
        }
         
      }catch (Exception e){
        JOptionPane.showMessageDialog(null, e);
      }
    
    }

